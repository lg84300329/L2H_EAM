public class Area {
    private int    area_id;//区域ID
    private String area_name;//区域名称
    private String area_code;//区域编码
    private String parent_area_code;//上级编码

    public int getArea_id() {
        return area_id;
    }

    public void setArea_id(int area_id) {
        this.area_id = area_id;
    }

    public String getArea_name() {
        return area_name;
    }

    public void setArea_name(String area_name) {
        this.area_name = area_name;
    }

    public String getArea_code() {
        return area_code;
    }

    public void setArea_code(String area_code) {
        this.area_code = area_code;
    }

    public String getParent_area_code() {
        return parent_area_code;
    }

    public void setParent_area_code(String parent_area_code) {
        this.parent_area_code = parent_area_code;
    }
}
